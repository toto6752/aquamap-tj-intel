const dicts: Record<Lang, Dict> = { en, ru, tj };

interface I18nCtx {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: string) => string;
}

const Ctx = createContext<I18nCtx | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>("en");

  useEffect(() => {
    try {
      const saved = localStorage.getItem("aquamap.lang") as Lang | null;
      if (saved && dicts[saved]) setLangState(saved);
    } catch { /* ignore */ }
  }, []);

  const setLang = useCallback((l: Lang) => {
    setLangState(l);
    try { localStorage.setItem("aquamap.lang", l); } catch { /* ignore */ }
    try { document.documentElement.lang = l === "tj" ? "tg" : l; } catch { /* ignore */ }
  }, []);

  const t = useCallback((key: string) => dicts[lang][key] ?? dicts.en[key] ?? key, [lang]);

  return <Ctx.Provider value={{ lang, setLang, t }}>{children}</Ctx.Provider>;
}

export function useI18n() {
  const ctx = useContext(Ctx);
  if (!ctx) {
    // Safe fallback — used by root error/404 boundaries that may render
    // outside the provider during SSR or initial mount.
    return {
      lang: "en" as Lang,
      setLang: () => {},
      t: (key: string) => dicts.en[key] ?? key,
    };
  }
  return ctx;
}
